                  items: const [
                    DropdownMenuItem(value: 'Passport Renewal', child: Text('Passport Renewal')),
                    DropdownMenuItem(value: 'NIC Card', child: Text('NIC Card')),
                    DropdownMenuItem(value: 'Driving License', child: Text('Driving License')),
                    DropdownMenuItem(value: 'Birth Certificate', child: Text('Birth Certificate')),
                  ],
                  onChanged: (value) => setDialogState(() => selectedService = value ?? selectedService),
                ),
              ],
            ),
          ),
          actions: [
            TextButton(
              onPressed: () => Navigator.pop(dialogContext),
              child: Text('cancel'.tr()),
            ),
            ElevatedButton(
              onPressed: () async {
                final name = nameController.text.trim();
                if (name.isEmpty) return;
                final nic = nicController.text.trim();
                final token = 'W-${(walkInQueue.length + 1).toString().padLeft(3, '0')}';
                Navigator.pop(dialogContext);
                await WebApiService.addToQueue(
                  token: token,
                  officeId: _receptionOffice,
                  citizenName: name,
                  citizenNic: nic.isNotEmpty ? nic : null,
                  service: selectedService,
                  paymentStatus: 'pending',
                  fee: 0,
                  officerName: 'Reception Officer',
                );
                await _loadWalkInsFromApi();
                await _loadReceptionStats();
                if (!mounted) return;
                ScaffoldMessenger.of(context).showSnackBar(
                  SnackBar(content: Text('web_walkin_registered_success'.tr()), backgroundColor: Colors.green),
                );
              },
              style: ElevatedButton.styleFrom(backgroundColor: const Color(0xFF1A56DB)),
              child: Text('web_register_button'.tr()),
            ),
          ],
        ),
      ),
    );
  }

  Future<void> _callNextWalkIn() async {
    if (walkInQueue.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('web_no_walkins_message'.tr()), backgroundColor: Colors.orange),
      );
      return;
    }
    final next = walkInQueue.first;
    await WebApiService.serveToken(next['token'] as String, 'Reception Officer');
    _notifyCitizenByNic(
      nic: next['nic'] as String?,
      title: 'Your Token is Called',
      message: 'Token ${next['token']} is now being served. Please proceed to the counter.',
    );
    await _loadWalkInsFromApi();
    await _loadReceptionStats();
    if (!mounted) return;
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Text('web_calling_to_counter'.tr(args: ['${next['name']}', '${next['token']}'])),
        backgroundColor: Colors.blue,
      ),
    );
  }


  Future<void> _loadReceptionStats() async {
    final stats = await WebApiService.getReceptionStats(_receptionOffice);
    if (!mounted || stats == null) return;
    setState(() {
      activeQueueCount = stats['activeQueue'] as int? ?? activeQueueCount;

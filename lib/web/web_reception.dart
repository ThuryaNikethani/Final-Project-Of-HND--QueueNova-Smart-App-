  /// Notifies the citizen identified by [nic] via the `notifications`
  /// collection (the same one the citizen app's Notifications screen reads
  /// live). Looks the uid up through `nic_index`, same as login does.
  Future<void> _notifyCitizenByNic({
    required String? nic,
    required String title,
    required String message,
  }) async {
    if (nic == null || nic.isEmpty) return;
    try {
      final indexDoc = await FirebaseFirestore.instance.collection('nic_index').doc(nic.toUpperCase()).get();
      final uid = indexDoc.data()?['uid'] as String?;
      if (uid == null) return;
      await FirebaseFirestore.instance.collection('notifications').add({
        'uid': uid,
        'title': title,
        'message': message,
        'type': 'appointment',
        'isRead': false,
        'createdAt': FieldValue.serverTimestamp(),
      });
    } catch (e) {
      debugPrint('_notifyCitizenByNic error: $e');
    }
  }

  void _processCheckIn(String qrData) {
    // The QR encodes the appointment details directly as JSON (self-contained
    // — works regardless of what network the citizen's phone is on, unlike a
    // URL to the backend's LAN address). Extract appointmentId from that.
    // Falls back to the URL-path form (an older format this briefly used)
    // and finally a raw token/citizen-substring match for any QR still in an
    // even older format.
    String lookupId = qrData;
    try {
      final decoded = jsonDecode(qrData);
      if (decoded is Map && decoded['appointmentId'] is String) {
        lookupId = decoded['appointmentId'] as String;
      }
    } catch (_) {
      try {
        final uri = Uri.parse(qrData);
        if (uri.pathSegments.isNotEmpty) {
          lookupId = uri.pathSegments.last;
        }
      } catch (_) {}
    }

    final appointment = todayAppointments.firstWhere(
      (apt) => apt['id'] == lookupId || apt['token'] == qrData || apt['citizen'].contains(qrData),
      orElse: () => {},
    );

    if (appointment.isNotEmpty && !appointment['checkedIn']) {
      setState(() {
        appointment['checkedIn'] = true;
        appointment['status'] = 'Checked In';
        scannedData = 'web_checkin_status_scanned'.tr(args: ['${appointment['citizen']}', '${appointment['token']}']);
        _scanResultKind = 'success';
      });

      final paymentStatus = appointment['paymentStatus'] == 'paid' ? 'web_payment_completed'.tr() : 'web_payment_pending_counter'.tr();
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('web_checkin_success_snackbar'.tr(args: ['${appointment['citizen']}', '${appointment['token']}', paymentStatus])),
          backgroundColor: Colors.green,
          behavior: SnackBarBehavior.floating,
          duration: const Duration(seconds: 3),
        ),
      );
      _showScanResultDialog(
        title: 'web_checkin_status_scanned_title'.tr(),
        color: Colors.green,
        icon: Icons.check_circle,
        rows: [
          ('web_col_citizen'.tr(), '${appointment['citizen']}'),
          ('qr_detail_token_label'.tr(), '${appointment['token']}'),
          ('web_col_payment'.tr(), paymentStatus),
        ],
      );
      _notifyCitizenByNic(
        nic: appointment['nic'] as String?,
        title: 'Checked In',
        message: 'You have been checked in for ${appointment['service']}. Token ${appointment['token']} is now active — please wait to be called.',
      );
      WebApiService.addToQueue(
        token: appointment['token'] as String,
        officeId: _receptionOffice,
        citizenName: appointment['citizen'] as String,
        citizenNic: appointment['nic'] as String?,
        service: appointment['service'] as String,
        paymentStatus: appointment['paymentStatus'] as String,
        fee: (appointment['fee'] as num).toDouble(),
        officerName: 'Reception Officer',
      );
    } else if (appointment.isEmpty) {
      setState(() {
        scannedData = 'web_invalid_qr_scanned'.tr();
        _scanResultKind = 'invalid';
      });
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('web_invalid_qr_snackbar'.tr()), backgroundColor: Colors.red),
      );
      _showScanResultDialog(
        title: 'web_invalid_qr_scanned'.tr(),
        color: Colors.red,
        icon: Icons.error,
        rows: const [],
      );
    } else {
      setState(() {
        scannedData = 'web_already_checked_in_scanned'.tr(args: ['${appointment['citizen']}']);
        _scanResultKind = 'already';
      });
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('web_already_checked_in_snackbar'.tr(args: ['${appointment['citizen']}'])), backgroundColor: Colors.orange),
      );
      _showScanResultDialog(
        title: 'web_already_checked_in_scanned'.tr(args: ['${appointment['citizen']}']),
        color: Colors.orange,
        icon: Icons.info,
        rows: [
          ('web_col_citizen'.tr(), '${appointment['citizen']}'),
          ('qr_detail_token_label'.tr(), '${appointment['token']}'),
        ],
      );
    }

    Future.delayed(const Duration(seconds: 2), () {
      if (mounted) {
        setState(() {
          scannedData = '';
          _scanResultKind = '';
        });
      }
    });
  }

  // Shows the scan outcome as a popup (in addition to the existing snackbar
  // and inline scanner-box text) so the officer gets a clear, unmissable
  // confirmation of who was checked in, their token, and payment status.
  void _showScanResultDialog({
    required String title,
    required Color color,
    required IconData icon,
    required List<(String, String)> rows,
  }) {
    showDialog(
      context: context,
      builder: (dialogContext) => AlertDialog(
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(20)),
        title: Row(
          children: [
            Icon(icon, color: color),
            const SizedBox(width: 10),
            Expanded(child: Text(title)),
          ],
        ),
        content: rows.isEmpty
            ? null
            : Column(
                mainAxisSize: MainAxisSize.min,
                children: rows
                    .map((row) => Padding(
                          padding: const EdgeInsets.symmetric(vertical: 4),
                          child: Row(
                            mainAxisAlignment: MainAxisAlignment.spaceBetween,
                            children: [
                              Text(row.$1, style: const TextStyle(color: Colors.grey)),
                              Text(row.$2, style: const TextStyle(fontWeight: FontWeight.w600)),
                            ],
                          ),
                        ))
                    .toList(),
              ),
        actions: [
          TextButton(
            onPressed: () => Navigator.pop(dialogContext),
            child: Text('ok'.tr()),
          ),
        ],
      ),
    );
  }

